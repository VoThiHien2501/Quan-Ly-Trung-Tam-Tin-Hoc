    private void tblLopHocMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblLopHocMouseClicked
        // TODO add your handling code here:
        int selectedRow = tblLopHoc.getSelectedRow();
        if (selectedRow == -1) {
            return;
        }
        maLopDangChon = tblLopHoc.getValueAt(selectedRow, 0).toString();

        String maLop = tblLopHoc.getValueAt(selectedRow, 0).toString();
        lblLopHoc.setText(maLop);

        try {
            HocVienDAO dao = new HocVienDAO();
            List<HocVien> list = dao.getHocVienByMaLop(maLop);

            DefaultTableModel model = (DefaultTableModel) tblHocVien.getModel();
            model.setRowCount(0); // Xóa dữ liệu cũ

            for (HocVien hv : list) {
                model.addRow(new Object[]{
                    hv.getMaHV(),
                    hv.getHoTen(),
                    hv.getNgaySinh(),
                    hv.getGioiTinh(),
                    hv.getSdt(),
                    hv.getDiaChi(),
                    hv.getNgayDangKy(),
                    hv.getMaLop()
                });
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi tải danh sách học viên: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_tblLopHocMouseClicked

    private void btnDangKyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangKyActionPerformed
        // TODO add your handling code here:
        // Lấy mã lớp từ dòng đang chọn trong bảng tblLopHoc
        int selectedRow = tblLopHoc.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một lớp học trước.");
            return;
        }
        String maLop = tblLopHoc.getValueAt(selectedRow, 0).toString(); // Cột 0 là MaLop

        // Lấy dữ liệu từ form nhập
        String maHV = txtMaHocVien.getText().trim();
        String hoTen = txtHoTen.getText().trim();
        Date ngaySinh = dateChooserNgaySinh.getDate();
        String gioiTinh = rdoNam.isSelected() ? "Nam" : "Nữ";
        String sdt = txtSoDT.getText().trim();
        String diaChi = txtAreaDiaChi.getText().trim();
        Date ngayDangKy = datechooserNgayDK.getDate();

        // Kiểm tra dữ liệu bắt buộc
        if (maHV.isEmpty() || hoTen.isEmpty() || ngaySinh == null || ngayDangKy == null || sdt.isEmpty() || diaChi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin học viên.");
            return;
        }

        try {
            // Tạo đối tượng học viên
            HocVien hv = new HocVien();
            hv.setMaHV(maHV);
            hv.setHoTen(hoTen);
            hv.setNgaySinh(new java.sql.Date(ngaySinh.getTime()));
            hv.setGioiTinh(gioiTinh);
            hv.setSdt(sdt);
            hv.setDiaChi(diaChi);
            hv.setNgayDangKy(new java.sql.Date(ngayDangKy.getTime()));
            hv.setMaLop(maLop);

            // Thêm học viên vào DB
            HocVienDAO dao = new HocVienDAO();
            dao.insertHocVien(hv);  // Phương thức bạn cần có trong DAO

            // Load lại danh sách học viên của lớp vừa chọn
            List<HocVien> list = dao.getHocVienByMaLop(maLop);
            DefaultTableModel model = (DefaultTableModel) tblHocVien.getModel();
            model.setRowCount(0); // Xóa dữ liệu cũ
            for (HocVien h : list) {
                model.addRow(new Object[]{
                    h.getMaHV(), h.getHoTen(), h.getNgaySinh(), h.getGioiTinh(),
                    h.getSdt(), h.getDiaChi(), h.getNgayDangKy(), h.getMaLop()
                });
            }

            JOptionPane.showMessageDialog(this, "Đăng ký học viên thành công!");
            
            // Cập nhật lại bảng lớp (có SoLgHV đã tăng)
int selectedKhoaHocRow = tblKhoaHoc.getSelectedRow();
if (selectedKhoaHocRow != -1) {
    String maKH = tblKhoaHoc.getValueAt(selectedKhoaHocRow, 0).toString(); // Cột 0 là MaKH
    loadLopTheoKhoaHoc(maKH); // Hàm bạn đã có để load lại danh sách lớp theo khóa học
}

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi đăng ký học viên: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnDangKyActionPerformed

