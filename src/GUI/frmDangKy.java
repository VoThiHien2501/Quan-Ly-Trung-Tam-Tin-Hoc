    public void loadTable() {
        try {
            KhoaHocDAO dao = new KhoaHocDAO();
            List<KhoaHoc> list = dao.getAllKhoaHoc();
            DefaultTableModel model = (DefaultTableModel) tblKhoaHoc.getModel();
            model.setRowCount(0); // Xóa dữ liệu cũ trên bảng

            for (KhoaHoc kh : list) {
                model.addRow(new Object[]{
                    kh.getMaKH(),
                    kh.getTenKH(),
                    kh.getNgayBatDau(),
                    kh.getNgayKetThuc(),
                    kh.getHocPhi(),
                    kh.getSoLgLop(),
                    kh.getSoBuoiHoc(),
                    kh.getMaNV()
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi tải dữ liệu: " + e.getMessage());
        }
    }
    private void loadLopTheoKhoaHoc(String maKH) {
    try {
        LopDAO dao = new LopDAO();
        List<Lop> list = dao.getLopByMaKH(maKH);

        DefaultTableModel model = (DefaultTableModel) tblLopHoc.getModel();
        model.setRowCount(0); // xóa dữ liệu cũ

        for (Lop lop : list) {
            model.addRow(new Object[]{
                lop.getMaLop(),
                lop.getTenLop(),
                lop.getSoLgHV(),
                lop.getMaCaHoc(),
                lop.getMaKH(),
                lop.getMaGV(),
                lop.getMaPH()
            });
        }

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Lỗi khi load danh sách lớp: " + e.getMessage());
    }
}

