    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        String maKH = txtTimKiem.getText().trim(); // Giả sử bạn có JTextField tên là txtTimKiem

        if (maKH.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã khóa học cần tìm.");
            return;
        }

        try {
            KhoaHocDAO dao = new KhoaHocDAO();
            KhoaHoc kh = dao.findByMaKH(maKH);

            DefaultTableModel model = (DefaultTableModel) tblKhoaHoc.getModel();
            model.setRowCount(0); // Xóa dữ liệu cũ trên bảng

            if (kh != null) {
                model.addRow(new Object[]{
                    kh.getMaKH(),
                    kh.getTenKH(),
                    kh.getNgayBatDau(),
                    kh.getNgayKetThuc(),
                    kh.getHocPhi(),
                    kh.getSoLgLop(),
                    kh.getSoBuoiHoc(),
                    kh.getMaNV()
                });
            } else {
                JOptionPane.showMessageDialog(this, "Không tìm thấy mã khóa học: " + maKH);
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi tìm kiếm: " + e.getMessage());
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

